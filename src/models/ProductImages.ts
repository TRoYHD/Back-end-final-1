import {
  Table,
  Model,
  Column,
  DataType,
  BelongsTo,
  ForeignKey
} from 'sequelize-typescript';
import Product from './Product';

@Table({
  timestamps: true,
  tableName: 'product_images'
})
export default class ProductImages extends Model {
  @Column({
    type: DataType.STRING,
    allowNull: false
  })
  image!: string;

  @ForeignKey(() => Product)
  @Column({
    type: DataType.INTEGER
  })
  product_id!: number;

  @BelongsTo(() => Product)
  product!: Product;
}
